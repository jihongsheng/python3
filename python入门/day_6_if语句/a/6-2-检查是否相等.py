# 大多数条件测试都将一个变量的当前值同特定值进行比较。最简单的条件测试检查变量的值是否与特定值相等：

# 我们首先使用一个等号将car 的值设置为'bmw'
cat = 'bnw'
# 接下来，使用两个等号（== ）检查car 的值是否为'bmw'
print(cat == 'bnw')
# 这个相等运算符 在它两边的值相等时返回True ，否则返回False 。在这个示例中，两边的值相等，因此Python返回True 。
print("-" * 80)


# 如果变量car 的值不是'bmw' ，上述测试将返回False ：
# 一个等号是陈述；可解读为“将变量car 的值设置为'audi'
cat = 'andi'
# 两个等号是发问；可解读为“变量car 的值是'bmw' 吗？”
print(cat == 'bnw')
# 大多数编程语言使用等号的方式都与这里演示的相同。

