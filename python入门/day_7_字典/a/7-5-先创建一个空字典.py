# 有时候，在空字典中添加键—值对是为了方便，而有时候必须这样做。为此，可先使用一对空的花
# 括号定义一个字典，再分行添加各个键—值对。例如，下例演示了如何以这种方式创建字典alien_0 ：
alien_0 = {}
alien_0['color'] = 'green'
alien_0['points'] = 5
print(alien_0)

# 这里首先定义了空字典alien_0 ，再在其中添加颜色和点数，得到前述示例一直在使用的字典：
# 使用字典来存储用户提供的数据或在编写能自动生成大量键—值对的代码时，通常都需要先定义一个空字典。
