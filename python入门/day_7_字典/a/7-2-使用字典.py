# 在Python中，字典 是一系列键—值对 。每个键 都与一个值相关联，你可以使用键来访问与之相关联的值。
# 与键相关联的值可以是数字、字符串、列表乃至字典。事实上，可将任何Python对象用作字典中的值。
# 在Python中，字典用放在花括号{} 中的一系列键—值对表示，如前面的示例所示：
alien_0 = {'color': 'green', 'points': 5}
# 键—值 对是两个相关联的值。指定键时，Python将返回与之相关联的值。键和值之间用冒号分隔，而键—
# 值对之间用逗号分隔。在字典中，你想存储多少个键—值对都可以。最简单的字典只有一个键—值对，如下
# 述修改后的字典alien_0 所示：
alien_0 = {'color': 'green'}
# 这个字典只存储了一项有关alien_0 的信息，具体地说是这个外星人的颜色。在这个字典中，字符串'color'
# 是一个键，与之相关联的值为'green' 。